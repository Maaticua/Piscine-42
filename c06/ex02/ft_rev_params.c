#include <unistd.h>

int	main(int ac, char *av[])
{
	int	i;
	int	l;

	i = 0;
	l = ac - 1;
	while (l > 0)
	{	
		while (av[l][i])
		{
			write(1, &av[l][i], 1);
			i++;
		}
		write(1, "\n", 1);
		i = 0;
		l--;
	}
	return (0);
}
